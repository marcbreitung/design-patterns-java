package DesignPatterns.Creational.Prototype;

public class FarmerTool {

    private String name;

    public FarmerTool() {
    }

    public FarmerTool(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
